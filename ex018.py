import math
angulo = int(input('Digite o ângulo que você deseja: '))
print('O ângulo de 30º tem o SENO de {:.2f}'.format(math.sin(math.radians(angulo))))
print('O ângulo de 30º tem o COSSENHO de {:.2f}'.format(math.cos(math.radians(angulo))))
print('O ângulo de 30º tem o TANGENTE de {:.2f}'.format(math.tan(math.radians(angulo))))
